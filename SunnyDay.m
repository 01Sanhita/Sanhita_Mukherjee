t=linspace(0,24,1000);
d=zeros(1,length(t));
s=cos((2*pi/24)*(t-12));
k=(s<0);
s(k)=0;
k=(s>0);
d(k)=1;
subplot(2,1,1);
plot(t,s,'m');
xlabel('Hour');
ylabel('Solar Flux');
title('Flux Model');
xlim([0 24]);
xt=[0 6 12 18 24];
set(gca,'XTick',xt);
grid;
subplot(2,1,2);
plot(t,d);
xlabel('Hour');
ylabel('Day/Night');
title('Perceptron Output');
xlim([0 24]);
xt=[0 6 12 18 24];
set(gca,'XTick',xt);
grid;